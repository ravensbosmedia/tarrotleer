export function vindKristal(slug: string): Kristal | undefined {
  return KRISTALLEN.find(k => k.slug === slug);
}

export function filterKristallen(query: string): Kristal[] {
  const q = query.toLowerCase();
  return KRISTALLEN.filter(k =>
    k.naam.toLowerCase().includes(q) ||
    k.kleur.some(kl => kl.includes(q)) ||
    k.element.toLowerCase().includes(q) ||
    k.chakra.toLowerCase().includes(q) ||
    k.zoekwoorden.some(w => w.includes(q))
  );
}
