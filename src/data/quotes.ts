export function getQuoteVanDaag(): Quote {
  const dag = new Date();
  const dagVanJaar = Math.floor((dag.getTime() - new Date(dag.getFullYear(), 0, 0).getTime()) / 86400000);
  return QUOTES[dagVanJaar % QUOTES.length];
}

export function getWillekeurigeQuote(): Quote {
  return QUOTES[Math.floor(Math.random() * QUOTES.length)];
}
